//画点 
//x:0~127
//y:0~63
//t:1 填充 0,清空				   
void OLED_DrawPoint(u8 x,u8 y,u8 t)
{
	u8 pos,bx,temp=0;

	if(x>127||y>63)
		return;//超出范围了.
	pos=7-y/8;
	bx=y%8;
	temp=1<<(7-bx);
	if(t)
		OLED_GRAM[x][pos]|=temp;
	else
		OLED_GRAM[x][pos]&=~temp;	    
}
//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void OLED_Fill(u8 x1,u8 y1,u8 x2,u8 y2,u8 dot)  
{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
	}													    
	OLED_Refresh_Gram();//更新显示
}

//----显示X宽*Y高的图标----------------------------------------------
void OLED_ShowImg(u8 x, u8 y, u8 *buff)
{
	u8 tmp, w, h, x0=x;
	u8 width, height;

    for(h=0; h<16; h++)
    {   
		for(height=0; height<FontHeight; height++)
		{
			x=x0;
			tmp=buff[h];
    		for(w=0; w<8; w++)
	    	{   
				for(width=0; width<FontWidth; width++)
				{
					if(tmp & 0x80)
						OLED_DrawPoint(x,y,FontColor);
					else
						OLED_DrawPoint(x,y,!FontColor);
					x++;
				}
				tmp<<=1;
			}
			y++;
		}
	}
}

