//显示字符串
//x,y:起点坐标  
//*p:字符串起始地址
//用16字体
void OLED_ShowString(u8 x,u8 y, u8 fontwidth, u8 fontheight, char *str)
{
#define MAX_CHAR_POSX 122
#define MAX_CHAR_POSY 58 
	const unsigned char *p, *pHzTab;
	u8 buff[16];
	u16 i;
	u16 HzTab_lenth;

	FontWidth = fontwidth;
	FontHeight = fontheight;	 

    while(*str!='\0')
    {       
        if(x>MAX_CHAR_POSX)
		{
			x=0;
			y+=16;
		}
        if(y>MAX_CHAR_POSY)
		{
			y=x=0;
			OLED_Clear();
		}
		//是ASC码
		if((int)*str < 0x80)
		{
			p = ASC16;
			p += (16 * (*str));
			memcpy(buff, p, 16);
			OLED_ShowImg(x, y, buff);
	        str++;
        	x+=(8*fontwidth);
		}
		else
		//是扩充16*16符号
		if((int)*str < 0xA1)
		{
			p = ASC16;
			p += (16 * (*str));
			for(i=0; i<16; i++)
			{
				buff[i]=*p;		p++;
				buff[i+16]=*p;	p++;
			}
			OLED_ShowImg(x, y, buff);
        	x+=(8*fontwidth);
			OLED_ShowImg(x, y, &buff[16]);
        	x+=(8*fontwidth);
	        str+=2;
		}
		else
		//是汉字
		{
			p = HZ16;
			pHzTab = HZTAB;		//指向简易汉字库索引
			HzTab_lenth = strlen((char*)HZTAB)/2;
			//查找汉字
			while(HzTab_lenth)
			{
//				HzVal=*((u16*)str);
				if(*((u16*)str) == *((u16*)pHzTab))
				{
					for(i=0; i<16; i++)
					{
						buff[i]=*p;		p++;
						buff[i+16]=*p;	p++;
					}
					OLED_ShowImg(x, y, buff);
		        	x+=(8*fontwidth);
					OLED_ShowImg(x, y, &buff[16]);
		        	x+=(8*fontwidth);
	    		    str+=2;
					break;
				}
				pHzTab+=2;
				p+=32;
				HzTab_lenth--;
			}
		}
    }  
}	   
